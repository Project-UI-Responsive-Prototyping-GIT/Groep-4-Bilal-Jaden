    <h1>Algemene Verordening Gegevensbescherming (AVG) Regels - Golden Dragon Chinese Restaurant</h1>

    <p>De volgende regels dienen als algemene richtlijnen voor gegevensbescherming:</p>

    <ol>
        <li>
            <strong>Toestemming voor Gegevensverwerking:</strong>
            <ul>
                <li>Het Golden Dragon Chinese Restaurant verkrijgt expliciete toestemming van klanten voordat persoonlijke gegevens worden verzameld.</li>
                <li>De toestemming wordt helder omschreven en omvat het doel van de gegevensverwerking.</li>
            </ul>
        </li>

        <li>
            <strong>Transparantie:</strong>
            <ul>
                <li>Het restaurant informeert klanten over het verzamelen en verwerken van persoonlijke gegevens via een duidelijk privacybeleid.</li>
                <li>Klanten worden geïnformeerd over hun rechten met betrekking tot gegevensbescherming.</li>
            </ul>
        </li>

        <li>
            <strong>Recht op Toegang en Rectificatie:</strong>
            <ul>
                <li>Klanten hebben het recht om hun persoonlijke gegevens in te zien en te corrigeren. Het restaurant zorgt voor een eenvoudige procedure voor het uitoefenen van deze rechten.</li>
            </ul>
        </li>

        <li>
            <strong>Beveiliging van Gegevens:</strong>
            <ul>
                <li>Het Golden Dragon Chinese Restaurant implementeert passende technische en organisatorische maatregelen om de beveiliging van persoonsgegevens te waarborgen.</li>
                <li>Persoonlijke gegevens worden opgeslagen op beveiligde servers met beperkte toegang.</li>
            </ul>
        </li>

        <li>
            <strong>Dataoverdraagbaarheid:</strong>
            <ul>
                <li>Klanten hebben het recht om hun persoonlijke gegevens op te vragen en over te dragen naar een andere dienstverlener.</li>
                <li>Het restaurant faciliteert deze overdracht indien nodig.</li>
            </ul>
        </li>

        <li>
            <strong>Meldplicht Datalekken:</strong>
            <ul>
                <li>Het Golden Dragon Chinese Restaurant heeft een procedure voor het melden van datalekken aan de toezichthoudende autoriteit en betrokken klanten, indien van toepassing.</li>
            </ul>
        </li>

        <li>
            <strong>Privacy by Design en Privacy by Default:</strong>
            <ul>
                <li>Bij het ontwikkelen van nieuwe processen en systemen wordt privacy geïntegreerd (Privacy by Design).</li>
                <li>Standaardinstellingen voor gegevensverwerking waarborgen de hoogste mate van privacy (Privacy by Default).</li>
            </ul>
        </li>

        <li>
            <strong>Functionaris voor Gegevensbescherming (FG):</strong>
            <ul>
                <li>Het restaurant bepaalt of het verplicht is een Functionaris voor Gegevensbescherming aan te stellen en doet dit indien nodig.</li>
            </ul>
        </li>

        <li>
            <strong>Bewaartermijn van Gegevens:</strong>
            <ul>
                <li>Persoonlijke gegevens worden niet langer bewaard dan nodig is voor het beoogde doel en conform wettelijke vereisten.</li>
            </ul>
        </li>

        <li>
            <strong>Training en Bewustwording:</strong>
            <ul>
                <li>Het personeel van het Golden Dragon Chinese Restaurant wordt getraind in gegevensbescherming en privacybewustzijn.</li>
            </ul>
        </li>
    </ol>

    <p>Deze regels dienen als algemene richtlijnen. Het is belangrijk dat het restaurant juridisch advies inwint om ervoor te zorgen dat het voldoet aan specifieke vereisten en wetgeving op het gebied van gegevensbescherming in hun jurisdictie.</p>
