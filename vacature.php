            function toggleEnlarged(element) {

                element.classList.toggle('enlarged');


                var isEnlarged = element.classList.contains('enlarged');

                // Show or hide the hidden text when the article is enlarged
                var hiddenText = element.querySelector('.hidden-text');
                if (hiddenText) {
                    hiddenText.style.display = isEnlarged ? 'block' : 'none';
                }

                // Apply custom styles when the article is enlarged
                if (isEnlarged) {
                    element.style.position = 'fixed';
                    element.style.top = '50%';
                    element.style.left = '50%';
                    element.style.transform = 'translate(-50%, -50%)';
                    element.style.width = '50%';
                    element.style.height = '100vh';
                    element.style.zIndex = '9999';
                } else {
                    // Reset custom styles when not enlarged
                    element.style.position = '';
                    element.style.top = '';
                    element.style.left = '';
                    element.style.transform = '';
                    element.style.width = '';
                    element.style.height = '';
                    element.style.zIndex = '';
                }
            }
